package io.zipcoder.interfaces;

public class Person {

    final Long id;
    String name;


    public Person(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}
